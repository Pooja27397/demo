package array_28;
// Java array to illustrate the use of declaration, instantiation
//& initialization of java array in a single line.
public class Testtarray1 {

public static void main(String[] args) {
	int a[] = {10,20,30,40};  //declaration, instantiation & initialization
	//printing array
	for(int i=0;i<a.length;i++) {  //length is the property of array
		System.out.println(a[i]);
	}
	}
}

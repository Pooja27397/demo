package hashset_linkedhash_treeset_36;

import java.util.HashSet;

public class HashSetDemo1 {

	public static void main(String[] args) {
		HashSet<Integer> hashset=new HashSet<Integer>();
		hashset.add(10);
		hashset.add(20);
		hashset.add(30);
		for(int j:hashset) {
			System.out.println(j);
		}
	}
	}


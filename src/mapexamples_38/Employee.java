package mapexamples_38;
//Can we take custom Employee class as key in hashMap.
public class Employee {
 int id=10;
String name="Rahul";
String Salary="6000";
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getSalary() {
	return Salary;
}
public void setSalary(String salary) {
	Salary = salary;
}
}
